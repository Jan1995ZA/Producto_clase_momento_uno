#Datos simple - simple data types
#String
print(type('hola')) #string
print(type("hola"))  ##string
print(type("""hola"""))   ###string
print(type('''hola'''))    #####string

#Numero
print(type(-10))     #numero negativo
print(type(+10))      #numero positivo
print(type(.123))       #decimal
print(type(1e-5))        #exponencial


