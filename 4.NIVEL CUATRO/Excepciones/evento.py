#Ques es un evento?
"""
Un evento en Python es una acción o suceso que puede ser detectado y manejado en un programa, 
generalmente utilizado en contextos interactivos como interfaces gráficas de usuario y aplicaciones web para 
responder a las acciones del usuario o cambios en el sistema.


1. Definición: Un evento es una acción o cambio que ocurre en un programa, como un clic de ratón, una pulsación de tecla, 
un cambio de estado en un sensor, etc.

2. Bibliotecas y Frameworks: Los eventos son manejados en Python a través de bibliotecas y frameworks específicos, 
como Tkinter para GUI, Django para aplicaciones web, y otras librerías según la aplicación.

3. Detectar Eventos: Para detectar un evento, se deben definir "manejadores de eventos" o "event handlers" 
que son funciones o métodos que se ejecutan cuando ocurre un evento específico.

4. Ejemplos de Eventos: Algunos ejemplos de eventos comunes incluyen eventos de clic de ratón, 
eventos de teclado, eventos de temporizador, eventos de entrada de usuario en formularios, 
eventos de cambio de valor en controles deslizantes, entre otros.

5. Registro de Eventos: Los eventos se registran y se asocian con los objetos que pueden generarlos,
como botones, ventanas, widgets, etc.

6. Respuesta a Eventos: Cuando se produce un evento, el manejador de eventos correspondiente se activa y 
ejecuta una acción o función específica.
"""