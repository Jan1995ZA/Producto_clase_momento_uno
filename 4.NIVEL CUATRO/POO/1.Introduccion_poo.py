#Que es la programacion orientada a objetos ?

"""
La programación orientada a objetos (POO) es un paradigma de programación que utiliza objetos y sus interacciones para diseñar aplicaciones y programas de computadora.

La POO se basa en varias conceptos clave, como la encapsulación, la abstracción, la herencia y el polimorfismo.

Estos conceptos permiten a los programadores crear programas más modulares, reutilizables y mantenibles.

En Python, la POO se implementa utilizando clases y objetos.

Una clase es una plantilla que define la estructura y el comportamiento de un objeto.

Un objeto es una instancia de una clase y tiene sus propias propiedades y métodos.

Los objetos pueden interactuar entre sí enviándose mensajes.

La POO es un paradigma de programación muy potente que se puede utilizar para crear una amplia variedad de aplicaciones.

Es especialmente adecuada para aplicaciones que requieren un alto grado de modularidad, reutilización y mantenibilidad.

"""