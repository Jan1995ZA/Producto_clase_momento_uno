#Que es un paquete?
"""
Un paquete Python es una colección de módulos Python que están relacionados entre sí. 
Los paquetes se utilizan para organizar el código Python y hacerlo más fácil de gestionar. 
Cada paquete tiene un nombre único, y los módulos dentro del paquete se importan utilizando el nombre del paquete. 
Por ejemplo, para importar el módulo `os` del paquete `os`, se utilizaría la siguiente declaración de importación:
​
import os
​

Los paquetes Python pueden ser instalados desde PyPI, el repositorio oficial de paquetes Python. 
Para instalar un paquete, se puede utilizar el siguiente comando:
​
pip install <nombre del paquete>
​

Una vez instalado un paquete, se puede importar en un script de Python utilizando la declaración de importación. 
Por ejemplo, para importar el módulo `os` del paquete `os`, se utilizaría la siguiente declaración de importación:
​
import os
​

Los paquetes Python son una forma útil de organizar el código Python y hacerlo más fácil de gestionar. 
Se pueden utilizar para importar módulos de otros paquetes, y se pueden instalar y desinstalar fácilmente.
"""